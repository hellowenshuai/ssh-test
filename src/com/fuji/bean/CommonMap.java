package com.fuji.bean;

/**
 * 通用实体类
 */
public class CommonMap {

    /** key */
    private String key;
    /** value */
    private String value;
    /**
     * 取得key
     * @return key
     */
    public String getKey() {
        return key;
    }
    /**
     * 设定key
     * @param String key
     */
    public void setKey(String key) {
        this.key = key;
    }
    /**
     * 取得value
     * @return value
     */
    public String getValue() {
        return value;
    }
    /**
     * 设定value
     * @param String value
     */
    public void setValue(String value) {
        this.value = value;
    }


}
